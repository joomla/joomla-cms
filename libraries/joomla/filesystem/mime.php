    public static function set($exts) {

        $catalog = self::$catalog;

        if (!isset($exts)) {
            $err = "No se ha especifidado ninguna extensión";
            return $err; 
        }

        $mimes = array();

        foreach($exts as $ext) {
            // cambia a minúsculas el string de la extensión
